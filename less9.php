    <h1>abstract class</h1>
    <p>sebuah class yang tidak dapat diinstansiasi, mendefinisikan intarface untuk kelas lain yang menjadi turunannya yakni kerangka dasarnya. Memiliki minimal 1 method abstrak. Digunakan dalam pewarisan untuk memaksakan implementasi method abstrak yang sama untuk semua kelas turunannya, Kelas abstrak boleh memiliki property / method regular (public, protected, private) / static method (static)</p>
    <p>Alasan penggunaan: representasi ide / konsep dasar, Composition over inheritance. Salah satu cara penerapan konsep Polimorphism. Sentralisasi logic. Mempermudah pengerjaan tim</p>


